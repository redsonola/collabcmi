      <h1 class="w3-center">About 'Skin Hunger'</h1><br>
      <h5 class="w3-center">Courtney Brown, Melanie Clemmons, Ira Greenberg, Brent Brimhall
</h5>
      <p class="w3-large">Participants can choose to be paired with a random partner or they send a link to a friend. As the participants interact through touch and movement, they create an audiovisual organism that progresses in complexity. Initial touch results in a long, held horn sound, and a visual organism begins to take shape. The pitch of the sound is determined by the location of the embrace. The participants can shape the sound and organism by moving in synchrony while they are touching and holding the touch longer. Each touch also evolves the organism and creates a short sound, which will repeat in its own rhythmic cycle, until it “dies”. The percussion parts are determined by the both the number of  short note cycles “alive” and the average movement of both the participants.
      <p class="w3-large">This work was created in response to the increased use of video communication during the 2020 pandemic, and the stress incurred due to a lack of touch as a result of social distancing. Lack of touch can result in touch starvation, sometimes referred to as skin hunger, which leads to increased levels of cortisol and feelings of social exclusion. While the remedy for touch starvation is skin to skin contact, we offer a digital alternative for those unable to obtain that remedy. Additionally, we hope that participants will enjoy a break from the confines of the rectangular grid of video communication.</p>
